<?php

namespace App\Entity;

use ApiPlatform\Core\Annotation\ApiResource;
use App\Repository\CommentaireRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ApiResource()
 * @ORM\Entity(repositoryClass=CommentaireRepository::class)
 */
class Commentaire
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="text")
     */
    private $description;

    /**
     * @ORM\Column(type="datetime")
     */
    private $writeAt;

    /**
     * @ORM\ManyToOne(targetEntity=Formateur::class, inversedBy="commentaires")
     */
    private $formateur;

    /**
     * @ORM\ManyToOne(targetEntity=FilDiscution::class, inversedBy="commentaires")
     */
    private $filDiscution;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getWriteAt(): ?\DateTimeInterface
    {
        return $this->writeAt;
    }

    public function setWriteAt(\DateTimeInterface $writeAt): self
    {
        $this->writeAt = $writeAt;

        return $this;
    }

    public function getFormateur(): ?Formateur
    {
        return $this->formateur;
    }

    public function setFormateur(?Formateur $formateur): self
    {
        $this->formateur = $formateur;

        return $this;
    }

    public function getFilDiscution(): ?FilDiscution
    {
        return $this->filDiscution;
    }

    public function setFilDiscution(?FilDiscution $filDiscution): self
    {
        $this->filDiscution = $filDiscution;

        return $this;
    }
}
